package com.spring_boot_travel.project.model;

public class FestivalVO {
	private String fvNo;
	private String fvName;
	private String fvDescript;
	private String fvDate;
	private String fvArea;
	
	
	public String getFvNo() {
		return fvNo;
	}
	public void setFvNo(String fvNo) {
		this.fvNo = fvNo;
	}
	public String getFvName() {
		return fvName;
	}
	public void setFvName(String fvName) {
		this.fvName = fvName;
	}
	public String getFvDescript() {
		return fvDescript;
	}
	public void setFvDescript(String fvDescript) {
		this.fvDescript = fvDescript;
	}
	public String getFvDate() {
		return fvDate;
	}
	public void setFvDate(String fvDate) {
		this.fvDate = fvDate;
	}
	public String getFvArea() {
		return fvArea;
	}
	public void setFvArea(String fvArea) {
		this.fvArea = fvArea;
	}
	

}
