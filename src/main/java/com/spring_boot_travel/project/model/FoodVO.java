package com.spring_boot_travel.project.model;

public class FoodVO {
	private String foodNo;
	private String foodName;
	private String foodAddress;
	private String foodTime;
	private String foodCallNumber;
	private String foodImg;
	private String ctgFoodId;
	public String getFoodNo() {
		return foodNo;
	}
	public void setFoodNo(String foodNo) {
		this.foodNo = foodNo;
	}
	public String getFoodName() {
		return foodName;
	}
	public void setFoodName(String foodName) {
		this.foodName = foodName;
	}
	public String getFoodAddress() {
		return foodAddress;
	}
	public void setFoodAddress(String foodAddress) {
		this.foodAddress = foodAddress;
	}
	public String getFoodTime() {
		return foodTime;
	}
	public void setFoodTime(String foodTime) {
		this.foodTime = foodTime;
	}
	public String getFoodCallNumber() {
		return foodCallNumber;
	}
	public void setFoodCallNumber(String foodCallNumber) {
		this.foodCallNumber = foodCallNumber;
	}
	public String getFoodImg() {
		return foodImg;
	}
	public void setFoodImg(String foodImg) {
		this.foodImg = foodImg;
	}
	public String getCtgFoodId() {
		return ctgFoodId;
	}
	public void setCtgFoodId(String ctgFoodId) {
		this.ctgFoodId = ctgFoodId;
	}
	
	
	
}
